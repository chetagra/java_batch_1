package com.company;

public class assignment_operators {
    public static void main(String[] args) {
        int a = 1;  // = acts as an assignment operator
        int b=1;
        b+=1; // =+ acts as an assignment operator;
        b-=1; // =- acts as an assignment operator;
        b*=1; // *= acts as an assignment opertor;
        b/=1; // /= acts as an assignment opertor;
        b%=1; // %= acts as an assignment operator;
    }
}
