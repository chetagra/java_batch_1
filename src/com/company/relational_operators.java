package com.company;

public class relational_operators {
    public static void main(String[] args) {
        // <,>,==,!=,<=,>=

        int a= 1;
        int b=2;
        boolean c= a>b;
        System.out.println(c);

        boolean d= a==b;
        System.out.println(d);

        boolean e = a!=b;
        System.out.println(e);

        boolean f= a<=b;
        System.out.println(f);

        boolean g= a>=b;
        System.out.println(g);

    }
}
